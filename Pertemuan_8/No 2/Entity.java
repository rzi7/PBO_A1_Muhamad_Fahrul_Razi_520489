public class Entity {
    private int lvl;
    private double hp;

    public void setLvl(int lvl) {
        this.lvl = lvl;
    }

    public void setHp(double hp) {
        this.hp = hp;
    }

    public int getLvl() {
        return lvl;
    }

    public double getHp() {
        return hp;
    }

}
